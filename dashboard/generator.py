def _get_equity_data(conn) -> list[dict]:
    """Get equity snapshots from events table."""
    rows = conn.execute(
        "SELECT timestamp, payload FROM events "
        "WHERE event_type IN ('cycle_complete', 'equity_snapshot') "
        "ORDER BY timestamp DESC LIMIT 200"
    ).fetchall()
    results = []
    for r in rows:
        try:
            payload = json.loads(r["payload"])
            equity = payload.get("equity") or payload.get("portfolio_value")
            if equity is not None:
                results.append({"timestamp": r["timestamp"], "equity": float(equity)})
        except (json.JSONDecodeError, TypeError):
            pass
    results.reverse()
    return results


def _get_strategies(conn) -> list[dict]:
    """Get all strategies with their current stage."""
    rows = conn.execute(
        "SELECT strategy_id, agent_id, namespace, stage, created_at, updated_at "
        "FROM strategy_registry ORDER BY updated_at DESC"
    ).fetchall()
    return [dict(r) for r in rows]


def _get_strategies_by_stage(conn) -> dict[str, list[dict]]:
    """Get all strategies grouped by stage with parsed JSON result columns."""
    rows = conn.execute(
        "SELECT strategy_id, agent_id, namespace, stage, config, "
        "backtest_results, robustness_results, paper_results, "
        "created_at, updated_at "
        "FROM strategy_registry ORDER BY updated_at DESC"
    ).fetchall()
    by_stage: dict[str, list[dict]] = {}
    for r in rows:
        entry = dict(r)
        for field in ("config", "backtest_results", "robustness_results", "paper_results"):
            if entry.get(field):
                try:
                    entry[field] = json.loads(entry[field])
                except (json.JSONDecodeError, TypeError):
                    entry[field] = None
            else:
                entry[field] = None
        stage = entry.get("stage", "unknown")
        by_stage.setdefault(stage, []).append(entry)
    return by_stage


def _get_research_notes(conn) -> list[dict]:
    """Get all research notes."""
    rows = conn.execute(
        "SELECT note_id, agent_id, status, observation, potential_edge, "
        "age_cycles, created_at FROM research_notes "
        "ORDER BY created_at DESC"
    ).fetchall()
    return [dict(r) for r in rows]


def _get_failed_cycles(conn) -> list[dict]:
    """Get recent failed cycles."""
    rows = conn.execute(
        "SELECT agent_id, cycle, timestamp, error, model_used "
        "FROM failed_cycles ORDER BY timestamp DESC LIMIT 20"
    ).fetchall()
    return [dict(r) for r in rows]


def _get_strategy_funnel(conn) -> dict[str, int]:
    """Get strategy count by stage."""
    rows = conn.execute(
        "SELECT stage, COUNT(*) as cnt FROM strategy_registry GROUP BY stage"
    ).fetchall()
    return {r["stage"]: r["cnt"] for r in rows}


def _get_risk_log(conn) -> list[dict]:
    """Get recent risk gate decisions."""
    rows = conn.execute(
        "SELECT id, created_at, agent_id, instruction_type, status, risk_check_result "
        "FROM instruction_queue ORDER BY id DESC LIMIT 50"
    ).fetchall()
    return [dict(r) for r in rows]


def _get_messages(conn) -> list[dict]:
    """Get recent agent messages."""
    rows = conn.execute(
        "SELECT created_at, from_agent, to_agent, message_type, priority, "
        "substr(payload, 1, 200) as payload_preview, status "
        "FROM agent_messages ORDER BY id DESC LIMIT 30"
    ).fetchall()
    return [dict(r) for r in rows]


def _get_feeds(conn) -> list[dict]:
    """Get latest supplementary feed values."""
    rows = conn.execute(
        "SELECT feed_name, timestamp, value, source "
        "FROM supplementary_feeds "
        "GROUP BY feed_name "
        "HAVING timestamp = MAX(timestamp) "
        "ORDER BY feed_name"
    ).fetchall()
    return [dict(r) for r in rows]


def _get_recent_trades(conn) -> list[dict]:
    """Get recent trades."""
    rows = conn.execute(
        "SELECT timestamp, agent_id, strategy_id, pair, action, size_usd, "
        "price, fill_price, paper, status "
        "FROM trades ORDER BY id DESC LIMIT 30"
    ).fetchall()
    return [dict(r) for r in rows]


def _get_system_state(conn) -> dict:
    """Get all system_state key-value pairs."""
    rows = conn.execute("SELECT key, value FROM system_state").fetchall()
    result = {}
    for r in rows:
        try:
            result[r["key"]] = json.loads(r["value"])
        except (json.JSONDecodeError, TypeError):
            result[r["key"]] = r["value"]
    return result


def _get_agent_summaries(conn, config: dict) -> list[dict]:
    """Build per-agent summary data."""
    agents_cfg = config.get("agents", {})
    enabled_agents = [
        aid for aid, acfg in agents_cfg.items()
        if isinstance(acfg, dict) and acfg.get("enabled", False)
    ]
    summaries = []
    for agent_id in enabled_agents:
        acfg = agents_cfg[agent_id]

        status_row = conn.execute(
            "SELECT value FROM system_state WHERE key = ?",
            (f"agent_status_{agent_id}",),
        ).fetchone()
        status = "active"
        if status_row:
            try:
                status = json.loads(status_row["value"]).get("status", "active")
            except (json.JSONDecodeError, TypeError):
                pass

        cycle_row = conn.execute(
            "SELECT MAX(cycle) as max_cycle FROM events WHERE agent_id = ?",
            (agent_id,),
        ).fetchone()
        cycles = cycle_row["max_cycle"] if cycle_row and cycle_row["max_cycle"] else 0

        last_success = conn.execute(
            "SELECT timestamp FROM events "
            "WHERE agent_id = ? AND event_type = 'cycle_complete' "
            "ORDER BY timestamp DESC LIMIT 1",
            (agent_id,),
        ).fetchone()
        if last_success:
            fail_row = conn.execute(
                "SELECT COUNT(*) as cnt FROM failed_cycles "
                "WHERE agent_id = ? AND timestamp > ?",
                (agent_id, last_success["timestamp"]),
            ).fetchone()
            consec_failures = fail_row["cnt"] if fail_row else 0
        else:
            fail_row = conn.execute(
                "SELECT COUNT(*) as cnt FROM failed_cycles WHERE agent_id = ?",
                (agent_id,),
            ).fetchone()
            consec_failures = fail_row["cnt"] if fail_row else 0

        cadence = acfg.get("cadence_hours", 4)
        wake_row = conn.execute(
            "SELECT payload FROM events "
            "WHERE agent_id = ? AND event_type = 'wake_schedule_update' "
            "ORDER BY timestamp DESC LIMIT 1",
            (agent_id,),
        ).fetchone()
        if wake_row:
            try:
                cadence = json.loads(wake_row["payload"]).get("base_cadence_hours", cadence)
            except (json.JSONDecodeError, TypeError):
                pass

        notes_row = conn.execute(
            "SELECT payload FROM events "
            "WHERE agent_id = ? AND event_type = 'cycle_notes' "
            "ORDER BY timestamp DESC LIMIT 1",
            (agent_id,),
        ).fetchone()
        last_notes = ""
        if notes_row:
            try:
                data = json.loads(notes_row["payload"])
                if isinstance(data, dict):
                    last_notes = data.get("notes", "")
                    if isinstance(last_notes, dict):
                        last_notes = last_notes.get("cycle_notes", str(last_notes))
                    elif not last_notes:
                        last_notes = data.get("cycle_notes", str(data))
                else:
                    last_notes = str(data)
            except (json.JSONDecodeError, TypeError):
                last_notes = str(notes_row["payload"])

        summaries.append({
            "agent_id": agent_id,
            "status": status,
            "cycles": cycles,
            "capital": acfg.get("capital_allocated", 0.0),
            "cadence": cadence,
            "consec_failures": consec_failures,
            "last_notes": last_notes,
        })
    return summaries


def _get_sirs(conn) -> list[dict]:
    """Get system improvement requests."""
    rows = conn.execute(
        "SELECT request_id, created_at, agent_id, cycle, title, problem, "
        "impact, category, priority, status, status_note "
        "FROM system_improvement_requests ORDER BY id DESC LIMIT 30"
    ).fetchall()
    return [dict(r) for r in rows]


def _get_owner_requests(conn) -> list[dict]:
    """Get owner requests from agents."""
    rows = conn.execute(
        "SELECT request_id, created_at, agent_id, cycle, type, urgency, "
        "title, description, blocked_work, status, resolution_note "
        "FROM owner_requests ORDER BY id DESC LIMIT 20"
    ).fetchall()
    return [dict(r) for r in rows]


