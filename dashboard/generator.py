def _fmt_metric(v, pct=False, color_thresh=None):
    """Format a numeric metric as an HTML table cell."""
    if v is None:
        return "<td style='color:#555'>-</td>"
    if pct:
        val_str = f"{v:.1f}%"
    elif isinstance(v, float):
        val_str = f"{v:.3f}"
    else:
        val_str = str(v)
    color = ""
    if color_thresh is not None and isinstance(v, (int, float)):
        color = " style='color:#4CAF50'" if v >= color_thresh else " style='color:#f44336'"
    return f"<td{color}>{val_str}</td>"


def _build_strategy_card(s: dict, stage_colors: dict) -> str:
    """Build an HTML card for a strategy showing cumulative results."""
    stage = s.get("stage", "unknown")
    scolor = stage_colors.get(stage, "#888")
    cfg = s.get("config") or {}
    bt = s.get("backtest_results") or {}
    rob = s.get("robustness_results") or {}
    paper = s.get("paper_results") or {}

    strategy_id = escape(str(s.get("strategy_id", "")))
    agent_id = escape(str(s.get("agent_id", "")))
    created = str(s.get("created_at", ""))[:10]
    updated = str(s.get("updated_at", ""))[:10]

    # Hypothesis info from config
    hypothesis_desc = escape(str(cfg.get("description", cfg.get("hypothesis", "")))[:200])
    kill_reason = escape(str(cfg.get("kill_reason", ""))[:120]) if stage == "graveyard" else ""
    killed_from = escape(str(cfg.get("killed_from_stage", ""))) if stage == "graveyard" else ""

    html = (
        f"<div style='background:#16213e;border-radius:8px;padding:14px;margin-bottom:10px;"
        f"border-left:3px solid {scolor};'>"
        f"<div style='display:flex;justify-content:space-between;align-items:center;margin-bottom:8px;'>"
        f"<b style='color:#e0e0e0;'>{strategy_id}</b>"
        f"<span style='font-size:0.8em;color:#888;'>{agent_id} &middot; {created} &rarr; {updated}</span>"
        f"</div>"
    )

    if hypothesis_desc:
        html += f"<div style='color:#aaa;font-size:0.85em;margin-bottom:8px;'>{hypothesis_desc}</div>"

    if stage == "graveyard" and kill_reason:
        html += (
            f"<div style='color:#f44336;font-size:0.85em;margin-bottom:8px;'>"
            f"Killed from <b>{killed_from}</b>: {kill_reason}</div>"
        )

    # Backtest results (shown for backtest+ stages)
    if bt:
        html += _build_results_row("Backtest", {
            "Return": (bt.get("total_return"), True, 0),
            "Sharpe": (bt.get("sharpe_ratio"), False, 0.5),
            "Max DD": (bt.get("max_drawdown"), True, None),
            "Win Rate": (bt.get("win_rate"), True, 50),
            "Trades": (bt.get("trade_count"), False, None),
            "Bench": (bt.get("benchmark_return"), True, None),
        })

    # Robustness results (shown for robustness+ stages)
    if rob and isinstance(rob, dict):
        html += _build_results_row("Robustness", {
            "Sharpe %ile": (rob.get("sharpe_percentile"), True, 90),
            "Return %ile": (rob.get("total_return_percentile"), True, 90),
            "Rand Sharpe %ile": (rob.get("random_entry_sharpe_percentile"), True, 90),
            "Rand Return %ile": (rob.get("random_entry_return_percentile"), True, 90),
        })

    # Paper results (shown for paper+ stages)
    if paper and isinstance(paper, dict):
        html += _build_results_row("Paper", {
            "PnL": (paper.get("pnl"), False, 0),
            "Return": (paper.get("total_return"), True, 0),
            "Sharpe": (paper.get("sharpe_ratio"), False, 0.5),
            "Trades": (paper.get("trade_count"), False, None),
            "Duration": (paper.get("duration_days"), False, None),
        })

    html += "</div>\n"
    return html


def _build_results_row(label: str, metrics: dict) -> str:
    """Build a compact metrics row for a strategy card."""
    items = ""
    for name, (val, is_pct, thresh) in metrics.items():
        if val is None:
            continue
        if is_pct and isinstance(val, (int, float)):
            val_str = f"{val:.1f}%"
        elif isinstance(val, float):
            val_str = f"{val:.3f}"
        else:
            val_str = str(val)
        color = "#e0e0e0"
        if thresh is not None and isinstance(val, (int, float)):
            color = "#4CAF50" if val >= thresh else "#f44336"
        items += f"<span style='margin-right:14px;'><span style='color:#888;'>{name}:</span> <b style='color:{color};'>{val_str}</b></span>"

    if not items:
        return ""
    return (
        f"<div style='font-size:0.82em;margin-bottom:4px;padding:4px 8px;background:#1a1a2e;border-radius:4px;'>"
        f"<span style='color:#81d4fa;font-weight:bold;margin-right:8px;'>{label}</span>{items}</div>"
    )


