"""Dashboard generator for the agentic quant trading system.

Produces a single-file HTML dashboard with inline CSS. Features:
- Executive summary card (generated by Haiku per cycle)
- CSS-only tabbed sections for strategies, research notes, operations
- Cumulative strategy results across lifecycle stages
- Strategy lifecycle funnel, equity curve, agent summaries
"""

import json
from datetime import datetime, timezone
from html import escape

from database.schema import get_db
from logging_config import get_logger

logger = get_logger("dashboard.generator")


def generate_dashboard(
    db_path: str,
    config: dict,
    output_path: str = "dashboard/index.html",
) -> str:
    """Generate a single-file HTML dashboard."""
    conn = get_db(db_path)

    equity_data = _get_equity_data(conn)
    strategies = _get_strategies(conn)
    strategies_by_stage = _get_strategies_by_stage(conn)
    research_notes = _get_research_notes(conn)
    failed_cycles = _get_failed_cycles(conn)
    funnel = _get_strategy_funnel(conn)
    risk_log = _get_risk_log(conn)
    messages = _get_messages(conn)
    feeds = _get_feeds(conn)
    trades = _get_recent_trades(conn)
    system_state = _get_system_state(conn)
    agent_summaries = _get_agent_summaries(conn, config)
    sirs = _get_sirs(conn)
    owner_requests = _get_owner_requests(conn)

    conn.close()

    generated_at = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    html = _build_html(
        equity_data=equity_data,
        strategies=strategies,
        strategies_by_stage=strategies_by_stage,
        research_notes=research_notes,
        failed_cycles=failed_cycles,
        funnel=funnel,
        risk_log=risk_log,
        messages=messages,
        feeds=feeds,
        trades=trades,
        system_state=system_state,
        agent_summaries=agent_summaries,
        sirs=sirs,
        owner_requests=owner_requests,
        config=config,
        generated_at=generated_at,
    )

    import os
    os.makedirs(os.path.dirname(output_path) or ".", exist_ok=True)

    with open(output_path, "w", encoding="utf-8") as f:
        f.write(html)

    logger.info("Dashboard generated at %s", output_path)
    return output_path


